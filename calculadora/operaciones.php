<?php
    function getSuma($a, $b){
        $result = $a + $b;
        echo "El resultado es: " . $result;
    }
    function getResta($a, $b){
        $result = $a - $b;
        echo "El resultado es: " . $result;
    }
    function getProducto($a, $b){
        $result = $a * $b;
        echo "El resultado es: " . $result;
    }
    function getDivision($a, $b){
        $result = $a / $b;
        echo "El resultado es: " . $result;
    }